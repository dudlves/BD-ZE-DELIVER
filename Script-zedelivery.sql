-- CONSULTAS COM INNER JOIN 
--Estoque do produto 
SELECT E.QTD_PRODUTO, P.NOME AS NOME_PRODUTO, F.NOME AS NOME_FORNECEDOR, L.NOME AS NOME_LOJA
FROM ESTOQUE E
INNER JOIN PRODUTO P ON P.COD_PRODUTO = E.COD_ESTOQUE
INNER JOIN LOJA L ON L.COD_LOJA = E.COD_ESTOQUE
INNER JOIN FORNECEDOR f ON F.COD_FORNECEDOR = P.COD_PRODUTO

--INFORMAÇÃO DE ENTREGA
SELECT P.STATUS, P.TEMPO_ESPERA, P.VLR_PEDIDO, P.DATA, C.NOME AS NOME_CLIENTE, C.ENDERECO,
L.NOME AS NOME_LOJA
FROM PEDIDO p 
INNER JOIN CLIENTE c ON C.COD_CLIENTE = P.COD_CLIENTE 
INNER JOIN LOJA L ON L.COD_LOJA = P.COD_LOJA

--CARRINHO DO CLIENTE
SELECT IP.QTD_PRODUTO, PE.STATUS, PE.TEMPO_ESPERA, PE.DATA, P.NOME AS NOME_PRODUTO, C.NOME AS NOME_CLIENTE, L.NOME AS NOME_LOJA
FROM ITENS_PEDIDO IP
INNER JOIN PEDIDO PE ON PE.COD_PEDIDO = IP.COD_PEDIDO 
INNER JOIN PRODUTO P ON P.COD_PRODUTO = IP.COD_PRODUTO
INNER JOIN CLIENTE C ON C.COD_CLIENTE = PE.COD_CLIENTE
INNER JOIN LOJA L ON L.COD_LOJA = PE.COD_LOJA

--INFORMAÇÃO DO PRODUTO (ESTOQUE LOJA PRODUTO FORNECEDOR)
SELECT E.QTD_PRODUTO, L.NOME AS NOME_LOJA, P.NOME AS NOME_PRODUTO, P.VLR_PRODUTO, F.NOME AS NOME_FORNECEDOR 
FROM ESTOQUE E
INNER JOIN LOJA L ON L.COD_LOJA = E.COD_LOJA 
INNER JOIN PRODUTO p ON P.COD_PRODUTO = E.COD_PRODUTO
INNER JOIN FORNECEDOR F ON F.COD_FORNECEDOR = P.COD_FORNECEDOR




